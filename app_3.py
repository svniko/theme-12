# app.py — Крок 3: Базовий Streamlit-застосунок

import streamlit as st

# ============================================================
# НАЛАШТУВАННЯ СТОРІНКИ
# ============================================================
# Має бути першою командою Streamlit у скрипті
st.set_page_config(
    page_title="AI Чатбот",
    page_icon="🤖",
    layout="centered",  # або "wide" для широкого макету
    initial_sidebar_state="expanded"
)

# ============================================================
# ЗАГОЛОВОК ТА ОПИС
# ============================================================
st.title("😂 AI Чатбот на Streamlit")
st.markdown("""
Це *демонстраційний* застосунок для вивчення **Streamlit**.
- Введіть повідомлення нижче, щоб почати діалог.
""")

# ============================================================
# БІЧНА ПАНЕЛЬ
# ============================================================
with st.sidebar:
    st.header("⚙️ Налаштування")

    # Інформація про модель
    st.info("Модель: **Gemini 3 Pro**")

    # Температура генерації
    temperature = st.slider(
        "Температура",
        min_value=0.0,
        max_value=1.0,
        value=0.7,
        step=0.1,
        help="Вища температура = більш креативні відповіді"
    )

    # Кнопка очищення
    if st.button("🗑️ Очистити історію", use_container_width=True):
        st.session_state.messages = []
        st.rerun()

    st.divider()
    st.caption("Створено для практичної роботи")

# ============================================================
# ІНІЦІАЛІЗАЦІЯ СТАНУ
# ============================================================
if "messages" not in st.session_state:
    st.session_state.messages = []

# ============================================================
# ВІДОБРАЖЕННЯ ІСТОРІЇ ЧАТУ
# ============================================================
for message in st.session_state.messages:
    with st.chat_message(message["role"]):
        st.write(message["content"])

# ============================================================
# ОБРОБКА НОВОГО ПОВІДОМЛЕННЯ
# ============================================================
prompt = st.chat_input("Введіть ваше повідомлення...")

if prompt:
    # Додаємо повідомлення користувача до історії
    st.session_state.messages.append({"role": "user", "content": prompt})

    # Відображаємо повідомлення користувача
    with st.chat_message("user"):
        st.write(prompt)

    # Генеруємо просту відповідь (поки без LLM)
    response = f"Ви написали: «{prompt}»\n\nТемпература: {temperature}"

    # Додаємо відповідь до історії
    st.session_state.messages.append({"role": "assistant", "content": response})

    # Відображаємо відповідь
    with st.chat_message("assistant"):
        st.write(response)